def x26c_func(x):
    def x26c_fprime(x):
        if (x % 32 == 0):
            return (x//32)*0x840 + 64
        return ((x//32)+1)*64
    delta = np.array([x26c_fprime(x2) for x2 in range(x)])
    ydata = np.cumsum(np.array([64] + list(delta))) - 64
    return ydata[x]

