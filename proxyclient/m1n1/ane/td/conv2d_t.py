    delta = []
    for count in range(7):
        unit = [(count+1)*(0x10*y)]*7 + [(0x10*y)+((count+1)*(0x280+(0x10*y)))]
        delta += unit
    delta += [0x280]*(x+20)
    delta = np.array(delta)
    ydata = np.cumsum(np.array([0x10*y] + list(delta)))
    return ydata[x]

